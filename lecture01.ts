// These All Are "ARTHIMETIC OPERATORS".
let x: number = 10;
let y: number = 5;
console.log("x = 10 , y = 20");
console.log("x + b =", x + y); // output: 15
console.log("x - b =", x - y); // output: 5
console.log("x * b =", x * y); // output: 50
console.log("x / b =", x / y); // output: 2
console.log("x % b =", x % y); // output: 0
console.log("2 ** 3 =", 2 ** 3); // output: 8

